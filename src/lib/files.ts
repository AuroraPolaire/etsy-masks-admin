import { ACCEPTED_FILE_EXTENSIONS } from '../constants';
import { readImageMetadata } from './imageMetadata';
import { slugify } from './slugify';

import type { FileExportGroups, ManagedFile, PromptItem, SubjectItem } from '../types';

export const getExpectedFilename = (subjectName: string): string => `${slugify(subjectName)}.png`;

export const isImageFile = (file: File | ManagedFile): boolean => {
  const name = 'name' in file ? file.name : '';
  const type = 'type' in file ? file.type : '';
  return type.startsWith('image/') || /\.(png|jpe?g|webp)$/i.test(name);
};

export const isAcceptedFile = (file: File): boolean => {
  const extension = file.name.split('.').pop()?.toLowerCase() ?? '';
  return ACCEPTED_FILE_EXTENSIONS.includes(extension);
};

export const formatBytes = (bytes: number): string => {
  if (bytes === 0) {
    return '0 B';
  }

  const units = ['B', 'KB', 'MB', 'GB'];
  const unitIndex = Math.min(Math.floor(Math.log(bytes) / Math.log(1024)), units.length - 1);
  const value = bytes / 1024 ** unitIndex;

  return `${value.toFixed(value >= 10 || unitIndex === 0 ? 0 : 1)} ${units[unitIndex] ?? 'B'}`;
};

export const fileToDataUrl = async (file: File | Blob): Promise<string> =>
  new Promise((resolve, reject) => {
    const reader = new FileReader();
    reader.onload = () => {
      if (typeof reader.result === 'string') {
        resolve(reader.result);
        return;
      }

      reject(new Error('Could not read file data as a data URL'));
    };
    reader.onerror = () => reject(new Error('Could not read file data'));
    reader.readAsDataURL(file);
  });

export const fileToText = async (file: File | Blob): Promise<string> =>
  new Promise((resolve, reject) => {
    const reader = new FileReader();
    reader.onload = () => {
      if (typeof reader.result === 'string') {
        resolve(reader.result);
        return;
      }

      reject(new Error('Could not read file as text'));
    };
    reader.onerror = () => reject(new Error('Could not read text file'));
    reader.readAsText(file);
  });

export const downloadBlob = (blob: Blob, fileName: string): void => {
  const objectUrl = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = objectUrl;
  link.download = fileName;
  document.body.append(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(objectUrl);
};

export const createGeneratedFile = (
  blob: Blob,
  name: string,
  kind: ManagedFile['kind'],
  imageMetadata?: ManagedFile['imageMetadata'],
): ManagedFile => {
  const now = new Date().toISOString();
  const file = new File([blob], name, { type: blob.type || 'application/octet-stream' });
  const objectUrl = imageMetadata ? URL.createObjectURL(file) : undefined;

  return {
    id: crypto.randomUUID(),
    file,
    name,
    originalName: name,
    size: file.size,
    type: file.type,
    addedAt: now,
    kind,
    ...(objectUrl ? { objectUrl } : {}),
    ...(imageMetadata ? { imageMetadata } : {}),
    reviewState: 'approved',
    reviewNotes: 'Generated by app.',
    explicitlyConfirmed: true,
  };
};

export const createManagedFile = async (
  file: File,
  subjects: SubjectItem[],
): Promise<ManagedFile> => {
  const now = new Date().toISOString();
  const imageMetadata = isImageFile(file) ? await readImageMetadata(file) : undefined;
  const matchingSubject = subjects.find(
    (subject) => getExpectedFilename(subject.name).toLowerCase() === file.name.toLowerCase(),
  );
  const objectUrl = isImageFile(file) ? URL.createObjectURL(file) : undefined;

  return {
    id: crypto.randomUUID(),
    file,
    name: file.name,
    originalName: file.name,
    size: file.size,
    type: file.type || 'application/octet-stream',
    addedAt: now,
    kind: 'uploaded',
    ...(objectUrl ? { objectUrl } : {}),
    ...(imageMetadata ? { imageMetadata } : {}),
    reviewState: 'pending',
    reviewNotes: '',
    ...(matchingSubject ? { mappedSubjectId: matchingSubject.id } : {}),
    explicitlyConfirmed: false,
  };
};

export const dedupeIncomingFiles = (
  existingFiles: ManagedFile[],
  incomingFiles: File[],
): { accepted: File[]; duplicates: string[]; unsupported: string[] } => {
  const existingNames = new Set(existingFiles.map((file) => file.name.toLowerCase()));
  const accepted: File[] = [];
  const duplicates: string[] = [];
  const unsupported: string[] = [];

  incomingFiles.forEach((file) => {
    const normalizedName = file.name.toLowerCase();

    if (!isAcceptedFile(file)) {
      unsupported.push(file.name);
      return;
    }

    if (existingNames.has(normalizedName)) {
      duplicates.push(file.name);
      return;
    }

    existingNames.add(normalizedName);
    accepted.push(file);
  });

  return { accepted, duplicates, unsupported };
};

